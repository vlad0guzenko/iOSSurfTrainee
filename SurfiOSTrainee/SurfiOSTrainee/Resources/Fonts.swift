import UIKit

extension UIFont {
    static var bold24: UIFont {
        return UIFont.boldSystemFont(ofSize: 24)
    }
    
    static var medium16: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: UIFont.Weight.medium)
    }
    
    static var medium14: UIFont {
        return UIFont.systemFont(ofSize: 14, weight: UIFont.Weight.medium)
    }
    
    static var regular14: UIFont {
        return UIFont.systemFont(ofSize: 14, weight: UIFont.Weight.regular)
    }
    
    static var regular16: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: UIFont.Weight.regular)
    }
}
